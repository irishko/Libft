char	*ft_strcat(char *dst, const char *app)
{
	int i;
	int k;

	i = 0;
	while (dst[i])
		i++;
	k = 0;
	while (app[k])
	{
		dst[i] = app[k];
		i++;
		k++;
	}
	dst[i] = '\0';
	return (dst);
}
