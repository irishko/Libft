#include "../includes/libft.h"

int		ft_minnum_mas(int *num, int len)
{
	int i;
	int j;

	i = 0;
	j = num[i];
	while (i < len - 1)
	{
		if (num[i] < j)
			j = num[i];
		i++;
	}
	return (j);
}
