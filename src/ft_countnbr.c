int	ft_countnbr(int n)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	if (n < 0)
	{
		k = 1;
		n = -n;
	}
	while (n > 9)
	{
		n = n / 10;
		i++;
	}
	return (i + 1 + k);
}
